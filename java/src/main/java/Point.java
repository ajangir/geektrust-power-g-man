public class Point {
    private int x;
    private int y;
    public Point()
    {
        x = 0;
        y = 0;
    }
    public Point(int value_0, int value_1) {
        x = value_0;
        y = value_1;
    }

    public int getX() {
        return x;
    }

    public void setX(int value) {
        x = value;
    }

    public int getY() {
        return y;
    }

    public void setY(int value) {
        y = value;
    }
}
